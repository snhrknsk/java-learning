package ch09.ex01;

public class Calc {

	public static void main(String[] args) {
		System.out.println( 5 + 2);
		System.out.println( 5 + (-2));
		System.out.println( 5 - 2);
		System.out.println( 5 - (-2));
		System.out.println( 5 * 2);
		System.out.println( 5 * (-2));
		System.out.println( 5 / 2);
		System.out.println( 5 / (-2));
	}

}
